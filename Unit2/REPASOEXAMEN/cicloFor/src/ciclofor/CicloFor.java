
package ciclofor;

public class CicloFor {

    public static void main(String[] args) {
       
        
        System.out.println("Forma Ascendente");
        for(int i=1;i<=10;i++)
        {
            System.out.print(i+"  ");
        }
        
          System.out.println("\nForma Descendente");
        for(int i=10;i>=1;i--)
        {
            System.out.print(i+"  ");
        }
        
        
    }
    
}
