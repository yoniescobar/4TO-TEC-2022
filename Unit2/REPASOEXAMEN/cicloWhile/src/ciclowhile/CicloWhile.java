
package ciclowhile;

public class CicloWhile {

    public static void main(String[] args) {
       int i=1;   
      
       System.out.println("Forma Ascendente");
       while(i<=10){
           System.out.println(i+" ");
           i++;
       }
       
       int j=10;  
      
       System.out.println("Forma Descendente");
       while(j>=1){
           System.out.println(j+" ");
           j--;
       }
       
    }
    
}
