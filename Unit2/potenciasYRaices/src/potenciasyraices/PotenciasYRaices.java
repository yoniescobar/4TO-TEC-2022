
package potenciasyraices;

public class PotenciasYRaices {

   
    public static void main(String[] args) {
       
        
        double numero=9 , raiz, potencia;
    
                   
         raiz = Math.sqrt(numero); // el Math.srt(numero) saca la raiz de un numero
         potencia = Math.pow(numero, 3);
         
         System.out.println("La raiz cuadrada es: "+raiz);
         System.out.println("La potencia al cubo es: "+potencia);
    
    }
    
}
